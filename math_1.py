def addition(num1,num2):
    answer = num1 +num2
    return answer

def sub(a,b):
    answer = a - b
    return answer

def multiply(c,d):
    answer = c * d
    return answer

def division(x,y):
    answer = x/ y
    return answer

def remainder(num5,num6):
    answer = num5% num6
    return answer